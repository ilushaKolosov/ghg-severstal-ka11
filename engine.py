# -*- coding: utf-8 -*-
"""
Расчётный движок климатического проекта
«Полезная утилизация вторичных энергетических ресурсов доменного газа
 с выработкой электроэнергии» (ПАО «Северсталь», ТЭЦ-ПВС, КА-11).

Реализованы формулы (1)-(13) Плана мониторинга проектной документации (PDD)
и Приказа Минприроды России от 27.05.2022 № 371.

Движок не зависит от сторонних библиотек — это «ядро» программы, которое
используется и Streamlit-интерфейсом (app.py), и проверочным скриптом (verify.py).

Обозначения параметров соответствуют таблице 7.6.1 PDD (П1...П31).
"""

from dataclasses import dataclass, field, asdict
from typing import List, Dict


# ----------------------------------------------------------------------------
#  Состав природного газа (объёмные доли, % об.) и число молей углерода
#  на моль компонента (Приложение № 2 к методике № 371, п. 1.6).
# ----------------------------------------------------------------------------

# Порядок компонентов фиксирован.
GAS_COMPONENTS = ["CH4", "C2H6", "C3H8", "C4H10", "C5H12", "C6+", "CO2", "прочие"]
# Число молей углерода n_C на моль компонента (П8).
N_CARBON = {"CH4": 1, "C2H6": 2, "C3H8": 3, "C4H10": 4,
            "C5H12": 5, "C6+": 6, "CO2": 1, "прочие": 0}


@dataclass
class Constants:
    """Фиксированные параметры (графа «фиксирован» таблицы 7.6.1)."""
    gwp_co2: float = 1.0            # П3  — ПГП CO2, т CO2-экв./т CO2
    of_ng: float = 1.0             # П6  — коэффициент окисления топлива, доля
    rho_co2: float = 1.8393        # П9  — плотность CO2, кг/м3 (t=20°C, p=760 мм рт.ст.)
    sfc_ng_bl: float = 17.40821828599454   # П14 — уд. расход прир. газа (КА №1-10), кг у.т./Гкал
    elg_tg_bl: float = 2850440.834         # П22 — выработка э/э ТЭЦ-ПВС по базовому сц., тыс. кВт*ч/год
    p_ow_bsl: float = 0.0525       # П26 — доля тепла на собств. нужды (базовый сц.), доля
    nd_bl: int = 365               # П29 — продолжительность года базового сценария, дней


@dataclass
class YearInput:
    """Измеряемые / переменные параметры за период y (графа «измеряется»)."""
    year: int
    fc_ng_proj: float        # П4  — расход прир. газа КА №1-11 (проект), тыс. м3
    ns_hws_pr: float         # П17 — выработка пара на ГВС (проект), Гкал
    ns_bf_pr: float          # П18 — выработка пара на производство (проект), Гкал
    ns_bfp_pr: float         # П19 — выработка пара на доменное произв. (проект), Гкал
    ns_elg_pr: float         # П24 — выработка пара на э/э (проект), Гкал
    elg_tg_pr: float         # П25 — выработка э/э ТЭЦ-ПВС (проект), тыс. кВт*ч
    nd_y: int                # П30 — продолжительность периода y, дней
    ncv_ng: float = 1.16     # П15 — коэф. перевода в у.т. (НТС), т у.т./тыс. м3
    ef_el: float = 328.6856666666667  # П28 — коэф. косв. выбросов э/э, кг CO2/МВт*ч
    # Объёмные доли компонентов природного газа (П7), % об.
    composition: Dict[str, float] = field(default_factory=lambda: dict(DEFAULT_COMPOSITION))


@dataclass
class YearResult:
    """Результаты расчёта за период y (промежуточные П-параметры и итоги)."""
    year: int
    ef_co2_ng: float    # П5  — коэф. выбросов CO2 от прир. газа, т CO2/тыс. м3 (ф.3)
    pe_fc: float        # П2  — выбросы проекта от сжигания газа, т CO2 (ф.2)
    pe: float           # П1  — выбросы по проектному сценарию, т CO2-экв. (ф.1)
    sfc_elg_bl: float   # П23 — уд. расход пара на э/э, Гкал/тыс. кВт*ч (ф.9)
    ns_elg_bl: float    # П20 — пар на э/э (базовый), Гкал (ф.8)
    ns_ow_bl: float     # П21 — пар на собств. нужды (базовый), Гкал (ф.10)
    sg_1_10_bl: float   # П16 — выработка пара КА №1-10 (базовый), Гкал (ф.7)
    fc_ng_bl: float     # П13 — расход прир. газа КА №1-10 (базовый), тыс. м3 (ф.6)
    be_fc: float        # П11 — выбросы базового сц. от газа, т CO2 (ф.5)
    ec_bl: float        # П27 — потребление э/э из сети (базовый), тыс. кВт*ч (ф.12)
    be_el: float        # П12 — выбросы базового сц. от покупной э/э, т CO2 (ф.11)
    be: float           # П10 — выбросы по базовому сценарию, т CO2-экв. (ф.4)
    er: float           # П31 — сокращение выбросов, т CO2-экв. (ф.13)

    def as_dict(self):
        return asdict(self)


def emission_factor_ng(composition: Dict[str, float], rho_co2: float) -> float:
    """
    Формула (3): коэффициент выбросов CO2 от сжигания природного газа.

        EF = ( Σ_i  W_i · n_C,i ) · ρ_CO2 · 10⁻²        [т CO2 / тыс. м3]

    где W_i — объёмная доля i-го компонента, % об.; n_C,i — число молей
    углерода на моль компонента; ρ_CO2 — плотность CO2, кг/м3.
    """
    s = sum(composition.get(c, 0.0) * N_CARBON[c] for c in GAS_COMPONENTS)
    return s * rho_co2 * 1e-2


def calc_year(inp: YearInput, const: Constants) -> YearResult:
    """Полный расчёт одного периода y по формулам (1)-(13)."""
    # --- общий коэффициент выбросов CO2 от природного газа (ф.3) ---
    ef = emission_factor_ng(inp.composition, const.rho_co2)

    # --- проектный сценарий ---
    pe_fc = inp.fc_ng_proj * ef * const.of_ng           # (2)
    pe = pe_fc * const.gwp_co2                           # (1)

    # --- базовый сценарий: пар и расход газа КА №1-10 ---
    sfc_elg_bl = inp.ns_elg_pr / inp.elg_tg_pr           # (9)
    ns_elg_bl = const.elg_tg_bl * sfc_elg_bl             # (8)
    p = const.p_ow_bsl
    ns_ow_bl = (inp.ns_bf_pr + inp.ns_hws_pr + inp.ns_bfp_pr + ns_elg_bl) * p / (1 - p)  # (10)
    sg_1_10_bl = inp.ns_hws_pr + inp.ns_bf_pr + inp.ns_bfp_pr + ns_elg_bl + ns_ow_bl     # (7)
    fc_ng_bl = sg_1_10_bl * const.sfc_ng_bl / inp.ncv_ng / 1000.0                        # (6)
    be_fc = fc_ng_bl * ef * const.of_ng                  # (5)

    # --- базовый сценарий: покупная электроэнергия ---
    ec_bl = inp.elg_tg_pr - const.elg_tg_bl * inp.nd_y / const.nd_bl   # (12)
    be_el = ec_bl * inp.ef_el / 1000.0                                 # (11)

    # --- итоги ---
    be = (be_fc + be_el) * const.gwp_co2                 # (4)
    er = be - pe                                         # (13)

    return YearResult(
        year=inp.year, ef_co2_ng=ef, pe_fc=pe_fc, pe=pe,
        sfc_elg_bl=sfc_elg_bl, ns_elg_bl=ns_elg_bl, ns_ow_bl=ns_ow_bl,
        sg_1_10_bl=sg_1_10_bl, fc_ng_bl=fc_ng_bl, be_fc=be_fc,
        ec_bl=ec_bl, be_el=be_el, be=be, er=er,
    )


def calc_period(inputs: List[YearInput], const: Constants = None) -> List[YearResult]:
    """Расчёт по всем периодам зачётного периода."""
    const = const or Constants()
    return [calc_year(i, const) for i in inputs]


