# ---------------------------------------------------------------- ПОД КАПОТОМ
st.markdown('<div class="sec"><span class="l"></span><h3>Что под капотом: формулы в общем виде</h3></div>',
            unsafe_allow_html=True)
st.markdown("""
<p class="note">Логика расчёта одинакова для обоих сценариев: <b>выбросы = объём сожжённого топлива ×
коэффициент выбросов</b>, плюс для базового сценария добавляются косвенные выбросы от электроэнергии,
закупаемой из сети. Ниже — формулы Плана мониторинга (нумерация (1)–(13) как в проектной документации).</p>
""", unsafe_allow_html=True)

st.markdown("""
<div class="formula">
<b>① Коэффициент выбросов CO₂ от газа</b> (из химсостава):<br>
&nbsp;&nbsp;EF = (Σ Wᵢ · n_C,i) · ρ_CO₂ · 10⁻²<br>
&nbsp;&nbsp;<span style="color:#8a99b5">Wᵢ — доля компонента, % об.; n_C,i — атомов углерода в молекуле; ρ_CO₂ — плотность CO₂.</span><br><br>

<b>② Проектный сценарий</b> (выбросы с КА-11):<br>
&nbsp;&nbsp;PE = FC_ПГ,1-11 · EF · OF · GWP &nbsp;&nbsp;<span style="color:#8a99b5">(формулы 1–2)</span><br>
&nbsp;&nbsp;<span style="color:#8a99b5">FC — расход природного газа; OF — коэф. окисления; GWP — потенциал глобального потепления.</span><br><br>

<b>③ Базовый сценарий</b> (как было бы без проекта):<br>
&nbsp;&nbsp;BE = (BE_топл + BE_эл) · GWP &nbsp;&nbsp;<span style="color:#8a99b5">(формула 4)</span><br>
&nbsp;&nbsp;BE_топл = FC_ПГ,1-10 · EF · OF &nbsp;&nbsp;<span style="color:#8a99b5">(формула 5) — выбросы от газа на старых котлах;</span><br>
&nbsp;&nbsp;FC_ПГ,1-10 = SG · SFC / К_ПГ / 1000 &nbsp;&nbsp;<span style="color:#8a99b5">(формула 6) — расход газа из выработки пара;</span><br>
&nbsp;&nbsp;BE_эл = EC · EF_эл / 1000 &nbsp;&nbsp;<span style="color:#8a99b5">(формула 11) — выбросы от покупной электроэнергии;</span><br>
&nbsp;&nbsp;EC = ELG_пр − ELG_баз · N_y/N_баз &nbsp;&nbsp;<span style="color:#8a99b5">(формула 12) — дефицит электроэнергии из сети.</span><br><br>

<b>④ Сокращение выбросов</b>:<br>
&nbsp;&nbsp;ER = BE − PE &nbsp;&nbsp;<span style="color:#8a99b5">(формула 13)</span>
</div>
""", unsafe_allow_html=True)

st.markdown("""
<div class="callout" style="margin-top:14px">
<b>Простыми словами.</b> Считаем, сколько CO₂ дал бы старый режим работы (BE), сколько даёт новый
режим с КА-11 (PE), и берём разницу (ER). Расход топлива переводится в выбросы через коэффициент EF,
который, в свою очередь, вычисляется из химического состава газа: чем больше в нём углеродосодержащих
компонентов (метан, этан и т.д.), тем выше выбросы на кубометр.
</div>
""", unsafe_allow_html=True)

